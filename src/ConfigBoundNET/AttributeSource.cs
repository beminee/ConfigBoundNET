#nullable enable

namespace ConfigBoundNET
{
    /// <summary>
    /// Marks a property on a <c>[ConfigSection]</c>-annotated type as holding
    /// a secret (connection string, API key, access token, etc.). The
    /// ConfigBoundNET source generator emits a <c>ToString()</c> /
    /// <c>PrintMembers()</c> override that replaces the value with
    /// <c>""***""</c> so secrets do not leak through record-default logging
